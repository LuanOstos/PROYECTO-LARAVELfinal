                        <p><h2 style="text-align:center;">AREQUIPA</h2></p>
                        <P>El departamento de Arequipa es uno de los 24 departamentos que conforman la República Constitucional del Perú y cuya capital es su homónima ciudad o también conocida como la “Ciudad Blanca”. Arequipa es considerada como una de las ciudades más importantes del Perú, llegando a ser declarada por la Unesco como “Patrimonio Cultural de la Humanidad” debido a su increíble arquitectura colonial.<br>La Catedral de la ciudad de Arequipa, al costado de la Plaza de Armas, es una de las primeras construcciones religiosa que se crearon en esta ciudad. Su construcción presenta un estilo neoclásico hecho a base de sillar. En el interior de este recinto se puede observar un enorme órgano de origen belga de incalculable valor. También se debe destacar que es una de las 70 iglesias del mundo que puede izar el estandarte del Vaticano.<br>Una característica poco convencional y muy impactante de esta tierra es que se encuentra rodeada de 4 volcanes: Ampato (6,288 metros sobre el nivel del mar), Chachani (6, 075 metros sobre el nivel del mar), Misti (5,825 metros sobre el nivel del mar),  y Pichu Pichu (5,644  metros sobre el nivel del mar).</P>
