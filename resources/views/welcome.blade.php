                        <p><h2 style="text-align:center;">SELVA</h2></p>
                        <p>En el lejano Este del Perú, allá donde concluyen las últimas estribaciones de la Cordillera de los Andes.se inicia la región de la Selva Baja, el gran llano amazónico, la floresta gigantesca e indomable, la región de los ríos—mares (el Amazonas, el Ucayali, el Marañón, el Urubamba, el Madre de Dios, el Ñapo, el Putumayo, etc.) que representa cerca del 50 % del territorio nacional.
                            En esta selva de gigantescos bosques vírgenes, no hay caminos. Mejor dicho, los caminos son los ríos, que abren las puertas del Perú hacia el Atlántico, pues los barcos de gran calado ingresan por el Amazonas—el río más caudaloso del mundo— hasta el puerto de Iquitos, capital del Departamente de Loreto, el más extenso del país.<br>La selva peruana inicia en las estribaciones orientales de la Cordillera Andina, y se extiende hasta las fronteras políticas peruanas con Brasil, Colombia y Ecuador, en el territorio de los departamentos peruanos de Loreto, Amazonas, San Martín, Ucayali y Madre de Dios. Todo ello, claro está, en el centro-oeste del subcontinente sudamericano.
                            Se divide en selva de montaña o selva alta (en la cordillera) y selva tropical o selva baja (en la llanura y el piedemonte)
                        </p>
